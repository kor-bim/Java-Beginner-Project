package dditair;

public class ReservationVO {
	private  String reserve_no;
	private  String reserve_seat;
	private  String reserve_price;
	private  String reserve_member;
	private  String reserve_pay;
	private  String reserve_date;
	public String getReserve_no() {
		return reserve_no;
	}
	public void setReserve_no(String reserve_no) {
		this.reserve_no = reserve_no;
	}
	public String getReserve_seat() {
		return reserve_seat;
	}
	public void setReserve_seat(String reserve_seat) {
		this.reserve_seat = reserve_seat;
	}
	public String getReserve_price() {
		return reserve_price;
	}
	public void setReserve_price(String reserve_price) {
		this.reserve_price = reserve_price;
	}
	public String getReserve_member() {
		return reserve_member;
	}
	public void setReserve_member(String reserve_member) {
		this.reserve_member = reserve_member;
	}
	public String getReserve_pay() {
		return reserve_pay;
	}
	public void setReserve_pay(String reserve_pay) {
		this.reserve_pay = reserve_pay;
	}
	public String getReserve_date() {
		return reserve_date;
	}
	public void setReserve_date(String reserve_date) {
		this.reserve_date = reserve_date;
	}
	
}
