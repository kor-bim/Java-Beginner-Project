package dditair;

public class ScheduleVO {
	private  String sc_no;
	private  String sc_airplane_no;
	private  String sc_departure;
	private  String sc_airport_d;
	private  String sc_airport_a;
	private  int sc_price;
	
	public String getSc_no() {
		return sc_no;
	}
	public void setSc_no(String sc_no) {
		this.sc_no = sc_no;
	}
	public String getSc_airplane_no() {
		return sc_airplane_no;
	}
	public void setSc_airplane_no(String sc_airplane_no) {
		this.sc_airplane_no = sc_airplane_no;
	}
	public String getSc_departure() {
		return sc_departure;
	}
	public void setSc_departure(String sc_departure) {
		this.sc_departure = sc_departure;
	}

	public String getSc_airport_d() {
		return sc_airport_d;
	}
	public void setSc_airport_d(String sc_airport_d) {
		this.sc_airport_d = sc_airport_d;
	}
	public String getSc_airport_a() {
		return sc_airport_a;
	}
	public void setSc_airport_a(String sc_airport_a) {
		this.sc_airport_a = sc_airport_a;
	}
	public int getSc_price() {
		return sc_price;
	}
	public void setSc_price(int sc_price) {
		this.sc_price = sc_price;
	}
}
