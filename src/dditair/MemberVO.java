package dditair;

public class MemberVO {
	
	private String mem_id;
	private String mem_pass;
	private String mem_name;
	private String mem_level;
	private String mem_mileage;
	private String mem_hp;
	private String mem_passport;
	
	public MemberVO() {
		this.mem_id = mem_id;
		this.mem_hp = mem_hp;
		this.mem_pass = mem_pass;
		this.mem_passport = mem_passport;
	}
	
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getMem_pass() {
		return mem_pass;
	}
	public void setMem_pass(String mem_pass) {
		this.mem_pass = mem_pass;
	}
	public String getMem_name() {
		return mem_name;
	}
	public void setMem_name(String mem_name) {
		this.mem_name = mem_name;
	}
	public String getMem_level() {
		return mem_level;
	}
	public void setMem_level(String mem_level) {
		this.mem_level = mem_level;
	}
	public String getMem_mileage() {
		return mem_mileage;
	}
	public void setMem_mileage(String string) {
		this.mem_mileage = string;
	}
	public String getMem_hp() {
		return mem_hp;
	}
	public void setMem_hp(String mem_hp) {
		this.mem_hp = mem_hp;
	}
	public String getMem_passport() {
		return mem_passport;
	}
	public void setMem_passport(String mem_passport) {
		this.mem_passport = mem_passport;
	}

	

	
}
